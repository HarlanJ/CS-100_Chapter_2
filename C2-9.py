C = float(input('Please enter the temperature in Celsius: '))		#Asks the user to input the temperature in Celsius
F = (9 / 5) * C +32		#Converts the Celsius to Fahrenheit

print(C , 'degrees Celsius is' , format(F, '.1f') , 'degrees Fahrenheit')		#Displays the converted Celsius and Fahrenheit